#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*str;
	int		longeur;
	int		i;

	i = 0;
	longeur = 0;
	while (src[longeur])
	{
		longeur++;
	}
	if (!(str = malloc(sizeof(char) * longeur + 1)))
		return (NULL);
	while (src[i])
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
