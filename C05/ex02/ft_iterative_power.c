int		ft_iterative_power(int nb, int power)
{
	int resu;
	int i;

	i = power;
	resu = 1;
	if (power < 0)
	{
		return (0);
	}
	else if (power == 0)
	{
		return (1);
	}
	while (i > 0)
	{
		resu = resu * nb;
		i--;
	}
	return (resu);
}
